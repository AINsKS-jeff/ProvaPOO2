    private void btmSalvar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btmSalvar1MouseClicked
        Campeonato obj = new Campeonato();
        
        obj.setCodigo(Integer.parseInt(txtCod.getText()));
        obj.setData(Integer.parseInt(txtAno.getText()));
        obj.setNome(txtNome.getText());
        
        CampeonatoDAO dao = new CampeonatoDAO();
        dao.cadastrarCampeonato(obj);
     
    }//GEN-LAST:event_btmSalvar1MouseClicked

