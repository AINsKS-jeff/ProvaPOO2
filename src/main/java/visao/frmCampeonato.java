public void listar(){
        CampeonatoDAO dao = new CampeonatoDAO();
        List<Campeonato> lista = dao.listarCampeonatos();
        DefaultTableModel dados = (DefaultTableModel)tbCampenato.getModel();
        dados.setNumRows(0);
        
        for(Campeonato c: lista){
            dados.addRow(new Object[]{
           c.getCodigo(),
            c.getData(),
            c.getNome()
        });
        }
        
        
    }
